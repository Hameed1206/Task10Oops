//1st QUE Parent class
public class Person {
    
    // Private variables
    private String name;
    private int age;
 
    // Default constructor initializing name and age
    public Person(){
        name = "Guvi";
        age = 10;
    }
    // Getter method for retrieving the name
    public String getName() {
        return name;
    }
    // Getter method for retrieving the age
    public int getAge() {
        return age;
    }
    }
